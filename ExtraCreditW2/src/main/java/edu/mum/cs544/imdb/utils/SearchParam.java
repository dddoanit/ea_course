package edu.mum.cs544.imdb.utils;

public class SearchParam {
  private String nameShow;
  private String genreShow;
  private float ratingShow;
  private String nameArtist;
  private String nameCharacter;
  private String nameDirector;
  public String getNameShow() {
    return nameShow;
  }
  public void setNameShow(String nameShow) {
    this.nameShow = nameShow;
  }
  public String getGenreShow() {
    return genreShow;
  }
  public void setGenreShow(String genreShow) {
    this.genreShow = genreShow;
  }
  public float getRatingShow() {
    return ratingShow;
  }
  public void setRatingShow(float ratingShow) {
    this.ratingShow = ratingShow;
  }
  public String getNameArtist() {
    return nameArtist;
  }
  public void setNameArtist(String nameArtist) {
    this.nameArtist = nameArtist;
  }
  public String getNameCharacter() {
    return nameCharacter;
  }
  public void setNameCharacter(String nameCharacter) {
    this.nameCharacter = nameCharacter;
  }
  public String getNameDirector() {
    return nameDirector;
  }
  public void setNameDirector(String nameDirector) {
    this.nameDirector = nameDirector;
  }
  
}
